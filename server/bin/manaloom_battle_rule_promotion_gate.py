#!/usr/bin/env python3
"""Report-only gate for battle-rule promotion readiness.

This job consumes drafts generated by `manaloom_battle_rule_review_queue.py`
and evaluates whether a draft is ready for manual promotion from needs_review to
verified. It never writes to PostgreSQL and never changes draft status by
itself.
"""

from __future__ import annotations

import argparse
import json
import os
import re
import sqlite3
import sys
from dataclasses import dataclass, field
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


SCRIPT_DIR = Path(__file__).resolve().parent
REPO_ROOT = SCRIPT_DIR.parents[1]
DEFAULT_OUTPUT_DIR = REPO_ROOT / "server/test/artifacts/battle_rule_promotion_gate_local"
DEFAULT_KNOWLEDGE_DB = REPO_ROOT / "server/test/artifacts/new_card_candidate_review_local/knowledge.db"


def utc_now() -> datetime:
    return datetime.now(timezone.utc)


def normalize_key(value: str | None) -> str:
    if not value:
        return ""
    return re.sub(r"[^a-z0-9]+", "_", value.strip().lower()).strip("_")


def parse_json(value: Any, default: Any) -> Any:
    if value is None:
        return default
    if isinstance(value, (dict, list)):
        return value
    if isinstance(value, str):
        text = value.strip()
        if not text:
            return default
        try:
            return json.loads(text)
        except Exception:
            return default
    return default


def bool_value(value: Any) -> bool:
    if isinstance(value, bool):
        return value
    if isinstance(value, (int, float)):
        return value != 0
    if isinstance(value, str):
        return value.strip().lower() in {"1", "true", "yes", "y", "on", "ok", "passed"}
    return False


def int_value(value: Any) -> int:
    try:
        if value is None or value == "":
            return 0
        return int(value)
    except (TypeError, ValueError):
        return 0


@dataclass
class DraftRecord:
    run_id: str
    card_name: str
    oracle_id: str | None
    set_code: str
    draft_rule_key: str
    proposed_status: str
    confidence: str
    draft: dict[str, Any]
    created_at: str


@dataclass
class GateItem:
    draft: DraftRecord
    decision: str
    blockers: list[str] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)
    evidence: dict[str, Any] = field(default_factory=dict)

    def to_json(self) -> dict[str, Any]:
        return {
            "card_name": self.draft.card_name,
            "oracle_id": self.draft.oracle_id,
            "set_code": self.draft.set_code,
            "draft_rule_key": self.draft.draft_rule_key,
            "source_review_run_id": self.draft.run_id,
            "proposed_status": self.draft.proposed_status,
            "confidence": self.draft.confidence,
            "decision": self.decision,
            "blockers": self.blockers,
            "warnings": self.warnings,
            "roles": self.draft.draft.get("roles", []),
            "effect_families": self.draft.draft.get("effect_families", []),
            "risk_flags": self.draft.draft.get("risk_flags", []),
            "evidence": self.evidence,
            "promotion_contract": {
                "postgres_write_allowed": False,
                "manual_review_required": True,
                "official_source_required": True,
                "focused_test_required": True,
                "replay_audit_required": True,
            },
        }


def table_exists(conn: sqlite3.Connection, name: str) -> bool:
    row = conn.execute(
        "SELECT 1 FROM sqlite_master WHERE type='table' AND name = ?",
        (name,),
    ).fetchone()
    return row is not None


def ensure_schema(conn: sqlite3.Connection) -> None:
    conn.execute(
        """
        CREATE TABLE IF NOT EXISTS new_card_battle_rule_promotion_gate_runs (
            run_id TEXT PRIMARY KEY,
            generated_at TEXT NOT NULL,
            evaluated_count INTEGER NOT NULL,
            eligible_count INTEGER NOT NULL,
            blocked_count INTEGER NOT NULL,
            summary_json TEXT NOT NULL
        )
        """
    )
    conn.execute(
        """
        CREATE TABLE IF NOT EXISTS new_card_battle_rule_promotion_gate_items (
            run_id TEXT NOT NULL,
            card_name TEXT NOT NULL,
            oracle_id TEXT,
            set_code TEXT,
            draft_rule_key TEXT NOT NULL,
            decision TEXT NOT NULL,
            blockers_json TEXT NOT NULL,
            warnings_json TEXT NOT NULL,
            gate_json TEXT NOT NULL,
            created_at TEXT NOT NULL,
            PRIMARY KEY (run_id, card_name, set_code, draft_rule_key)
        )
        """
    )
    conn.commit()


def load_latest_drafts(conn: sqlite3.Connection, limit: int) -> list[DraftRecord]:
    if not table_exists(conn, "new_card_battle_rule_review_drafts"):
        return []
    rows = conn.execute(
        """
        SELECT
            run_id,
            card_name,
            oracle_id,
            set_code,
            draft_rule_key,
            proposed_status,
            confidence,
            draft_json,
            created_at
        FROM new_card_battle_rule_review_drafts
        WHERE run_id = (
            SELECT run_id
            FROM new_card_battle_rule_review_runs
            ORDER BY generated_at DESC
            LIMIT 1
        )
        ORDER BY card_name, set_code, draft_rule_key
        LIMIT ?
        """,
        (limit,),
    ).fetchall()
    return [
        DraftRecord(
            run_id=str(row[0]),
            card_name=str(row[1]),
            oracle_id=str(row[2]) if row[2] else None,
            set_code=str(row[3] or ""),
            draft_rule_key=str(row[4]),
            proposed_status=str(row[5] or ""),
            confidence=str(row[6] or ""),
            draft=parse_json(row[7], {}),
            created_at=str(row[8] or ""),
        )
        for row in rows
    ]


def resolve_evidence_path(
    explicit_path: str | None,
    *,
    db_path: Path,
    output_dir: Path,
) -> Path | None:
    if explicit_path:
        return Path(explicit_path)

    candidates: list[Path] = []
    focused_dir = os.environ.get("MANALOOM_BATTLE_RULE_FOCUSED_EVIDENCE_DIR")
    if focused_dir:
        candidates.append(Path(focused_dir) / "latest_evidence.json")
    artifact_root = os.environ.get("MANALOOM_OPS_ARTIFACT_DIR")
    if artifact_root:
        candidates.append(
            Path(artifact_root)
            / "battle_rule_focused_evidence"
            / "latest_evidence.json"
        )
    candidates.append(output_dir.parent / "battle_rule_focused_evidence" / "latest_evidence.json")
    if db_path.parent.exists():
        candidates.extend(
            sorted(db_path.parent.glob("*/battle_rule_focused_evidence/latest_evidence.json"))
        )

    for candidate in candidates:
        if candidate.is_file():
            return candidate
    return None


def load_evidence(path: Path | None) -> dict[str, Any]:
    if path is None:
        return {}
    evidence_path = path
    if not evidence_path.is_file():
        raise FileNotFoundError(str(path))
    payload = json.loads(evidence_path.read_text(encoding="utf-8"))
    if not isinstance(payload, dict):
        raise ValueError("evidence file must contain a JSON object")
    return payload


def evidence_for_draft(draft: DraftRecord, evidence: dict[str, Any]) -> dict[str, Any]:
    candidates: list[dict[str, Any]] = []
    for section, key in (
        ("by_draft_rule_key", draft.draft_rule_key),
        ("by_oracle_id", draft.oracle_id or ""),
        ("by_card_name", draft.card_name),
    ):
        section_value = evidence.get(section)
        if not isinstance(section_value, dict):
            continue
        item = section_value.get(key) or section_value.get(normalize_key(key))
        if isinstance(item, dict):
            candidates.append(item)
    default = evidence.get("default")
    if isinstance(default, dict):
        candidates.insert(0, default)
    merged: dict[str, Any] = {}
    for candidate in candidates:
        merged.update(candidate)
    return merged


def evaluate_draft(draft: DraftRecord, evidence: dict[str, Any]) -> GateItem:
    blockers: list[str] = []
    warnings: list[str] = []
    draft_payload = draft.draft
    risk_flags = {str(item) for item in draft_payload.get("risk_flags", [])}

    if draft.proposed_status != "needs_review":
        blockers.append(f"unexpected_proposed_status:{draft.proposed_status}")
    if not draft.oracle_id:
        blockers.append("missing_oracle_id")
    if not str(draft_payload.get("oracle_text_excerpt") or "").strip():
        blockers.append("missing_oracle_text_excerpt")
    if "missing_oracle_text" in risk_flags:
        blockers.append("missing_oracle_text")

    if not bool_value(evidence.get("official_source_reviewed")):
        blockers.append("missing_official_source_review")
    if not bool_value(evidence.get("focused_test_passed")):
        blockers.append("missing_focused_test_evidence")
    if not bool_value(evidence.get("replay_audit_passed")):
        blockers.append("missing_replay_audit_evidence")
    if int_value(evidence.get("critical_findings")) > 0:
        blockers.append("critical_replay_findings_present")
    if int_value(evidence.get("high_findings")) > 0:
        blockers.append("high_replay_findings_present")

    if draft.confidence == "low":
        warnings.append("low_confidence_requires_extra_manual_review")
    if "multi_role_complexity" in risk_flags:
        warnings.append("multi_role_complexity")
    if not evidence.get("official_sources"):
        warnings.append("official_sources_not_listed")
    if not evidence.get("focused_test_refs"):
        warnings.append("focused_test_refs_not_listed")
    if not evidence.get("replay_audit_refs"):
        warnings.append("replay_audit_refs_not_listed")

    decision = "blocked"
    if not blockers:
        decision = "eligible_for_manual_verified_promotion"

    return GateItem(
        draft=draft,
        decision=decision,
        blockers=sorted(set(blockers)),
        warnings=sorted(set(warnings)),
        evidence=evidence,
    )


def summarize(run_id: str, generated_at: str, items: list[GateItem]) -> dict[str, Any]:
    decisions: dict[str, int] = {}
    blockers: dict[str, int] = {}
    warnings: dict[str, int] = {}
    for item in items:
        decisions[item.decision] = decisions.get(item.decision, 0) + 1
        for blocker in item.blockers:
            blockers[blocker] = blockers.get(blocker, 0) + 1
        for warning in item.warnings:
            warnings[warning] = warnings.get(warning, 0) + 1
    eligible = decisions.get("eligible_for_manual_verified_promotion", 0)
    blocked = decisions.get("blocked", 0)
    return {
        "run_id": run_id,
        "generated_at": generated_at,
        "mode": "sqlite_operational_cache",
        "dry_run": True,
        "evaluated_count": len(items),
        "eligible_count": eligible,
        "blocked_count": blocked,
        "decisions": decisions,
        "blockers": blockers,
        "warnings": warnings,
        "notes": [
            "report_only_no_pg_writes",
            "no_auto_promotion",
            "manual_review_required",
            "official_source_focused_test_replay_required",
        ],
    }


def persist(conn: sqlite3.Connection, summary: dict[str, Any], items: list[GateItem]) -> None:
    ensure_schema(conn)
    run_id = summary["run_id"]
    generated_at = summary["generated_at"]
    conn.execute(
        """
        INSERT OR REPLACE INTO new_card_battle_rule_promotion_gate_runs (
            run_id, generated_at, evaluated_count, eligible_count, blocked_count,
            summary_json
        ) VALUES (?, ?, ?, ?, ?, ?)
        """,
        (
            run_id,
            generated_at,
            summary["evaluated_count"],
            summary["eligible_count"],
            summary["blocked_count"],
            json.dumps(summary, sort_keys=True),
        ),
    )
    for item in items:
        payload = item.to_json()
        conn.execute(
            """
            INSERT OR REPLACE INTO new_card_battle_rule_promotion_gate_items (
                run_id, card_name, oracle_id, set_code, draft_rule_key,
                decision, blockers_json, warnings_json, gate_json, created_at
            ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            """,
            (
                run_id,
                item.draft.card_name,
                item.draft.oracle_id,
                item.draft.set_code,
                item.draft.draft_rule_key,
                item.decision,
                json.dumps(item.blockers, sort_keys=True),
                json.dumps(item.warnings, sort_keys=True),
                json.dumps(payload, sort_keys=True),
                generated_at,
            ),
        )
    conn.commit()


def render_markdown(summary: dict[str, Any], items: list[GateItem]) -> str:
    lines = [
        "# Battle Rule Promotion Gate",
        "",
        f"- Run: `{summary['run_id']}`",
        f"- Generated at: `{summary['generated_at']}`",
        f"- Evaluated: `{summary['evaluated_count']}`",
        f"- Eligible for manual verified promotion: `{summary['eligible_count']}`",
        f"- Blocked: `{summary['blocked_count']}`",
        "",
        "## Blockers",
        "",
    ]
    if summary["blockers"]:
        for blocker, count in sorted(summary["blockers"].items()):
            lines.append(f"- `{blocker}`: {count}")
    else:
        lines.append("- none")
    lines.extend(["", "## Items", ""])
    if not items:
        lines.append("No battle rule drafts found.")
    else:
        lines.append("| Card | Set | Decision | Confidence | Blockers | Warnings |")
        lines.append("| --- | --- | --- | --- | --- | --- |")
        for item in items[:120]:
            lines.append(
                "| {card} | {set_code} | `{decision}` | `{confidence}` | {blockers} | {warnings} |".format(
                    card=item.draft.card_name.replace("|", "\\|"),
                    set_code=item.draft.set_code,
                    decision=item.decision,
                    confidence=item.draft.confidence,
                    blockers=", ".join(item.blockers).replace("|", "\\|"),
                    warnings=", ".join(item.warnings).replace("|", "\\|"),
                )
            )
    lines.extend(
        [
            "",
            "## Safety Contract",
            "",
            "- This gate never writes to PostgreSQL.",
            "- `eligible_for_manual_verified_promotion` is not automatic promotion.",
            "- Verification requires official source review, focused test evidence, and replay/audit evidence.",
            "- Any critical/high replay finding blocks promotion.",
            "",
        ]
    )
    return "\n".join(lines)


def write_artifacts(output_dir: Path, run_id: str, summary: dict[str, Any], items: list[GateItem]) -> None:
    output_dir.mkdir(parents=True, exist_ok=True)
    run_dir = output_dir / run_id
    run_dir.mkdir(parents=True, exist_ok=True)
    item_payloads = [item.to_json() for item in items]
    for path in (run_dir / "summary.json", output_dir / "latest_summary.json"):
        path.write_text(json.dumps(summary, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    for path in (run_dir / "items.json", output_dir / "latest_items.json"):
        path.write_text(json.dumps(item_payloads, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    report = render_markdown(summary, items)
    for path in (run_dir / "report.md", output_dir / "latest_report.md"):
        path.write_text(report, encoding="utf-8")


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Report-only battle rule promotion gate")
    parser.add_argument("--knowledge-db", default=os.environ.get("MANALOOM_KNOWLEDGE_DB") or os.environ.get("HERMES_KNOWLEDGE_DB"))
    parser.add_argument("--output-dir", default=os.environ.get("MANALOOM_BATTLE_RULE_PROMOTION_GATE_DIR"))
    parser.add_argument("--evidence-file", default=os.environ.get("MANALOOM_BATTLE_RULE_PROMOTION_EVIDENCE_FILE"))
    parser.add_argument("--limit", type=int, default=int(os.environ.get("MANALOOM_BATTLE_RULE_PROMOTION_GATE_LIMIT", "250")))
    return parser.parse_args(argv)


def run(args: argparse.Namespace) -> dict[str, Any]:
    db_path = Path(args.knowledge_db or DEFAULT_KNOWLEDGE_DB)
    output_dir = Path(
        args.output_dir
        or os.environ.get("MANALOOM_OPS_ARTIFACT_DIR", "")
        or DEFAULT_OUTPUT_DIR
    )
    if output_dir.name != "battle_rule_promotion_gate":
        output_dir = output_dir / "battle_rule_promotion_gate"
    generated_at = utc_now().isoformat(timespec="seconds")
    run_id = "battle_rule_promotion_gate_" + utc_now().strftime("%Y%m%d_%H%M%S_%f")
    evidence_path = resolve_evidence_path(
        args.evidence_file,
        db_path=db_path,
        output_dir=output_dir,
    )
    evidence = load_evidence(evidence_path)

    if not db_path.exists():
        summary = summarize(run_id, generated_at, [])
        summary["blocked_reason"] = "knowledge_db_missing"
        if evidence_path is not None:
            summary["evidence_file"] = str(evidence_path)
        write_artifacts(output_dir, run_id, summary, [])
        print("MANALOOM_BATTLE_RULE_PROMOTION_GATE " + json.dumps(summary, sort_keys=True))
        return summary

    conn = sqlite3.connect(db_path)
    try:
        ensure_schema(conn)
        drafts = load_latest_drafts(conn, args.limit)
        items = [
            evaluate_draft(draft, evidence_for_draft(draft, evidence))
            for draft in drafts
        ]
        summary = summarize(run_id, generated_at, items)
        if evidence_path is not None:
            summary["evidence_file"] = str(evidence_path)
        persist(conn, summary, items)
    finally:
        conn.close()
    write_artifacts(output_dir, run_id, summary, items)
    print(
        "MANALOOM_BATTLE_RULE_PROMOTION_GATE "
        + json.dumps(
            {
                "run_id": run_id,
                "evaluated_count": summary["evaluated_count"],
                "eligible_count": summary["eligible_count"],
                "blocked_count": summary["blocked_count"],
                "output_dir": str(output_dir),
                "knowledge_db": str(db_path),
            },
            sort_keys=True,
        )
    )
    return summary


def main(argv: list[str] | None = None) -> int:
    try:
        run(parse_args(argv))
    except Exception as exc:
        print(f"MANALOOM_BATTLE_RULE_PROMOTION_GATE_FAILED {type(exc).__name__}: {exc}", file=sys.stderr)
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
