  VALUES
    ('goldspan dragon', 'Goldspan Dragon', '09b0aa8dc38ce303b42a28e2aece420e', 'battle_rule_v1:dfaa0b9820c90a7835fb1fcb506ae9f3', '{"ability_kind":"triggered","attack_or_becomes_target_create_treasure":true,"attack_trigger_create_treasure":true,"battle_model_scope":"goldspan_dragon_attack_or_target_treasure_double_mana_v1","becomes_spell_target_create_treasure":true,"controlled_treasures_add_two_mana":true,"effect":"creature","flying":true,"haste":true,"power":4,"toughness":4,"treasure_count":1,"treasure_mana_value":2}'::jsonb, '{"category":"board_presence","effect":"creature","timing":"battlefield"}'::jsonb, 'curated', 0.94, 'verified', 'auto', 'XMage batch proposal: exact local XMage class GoldspanDragon mapped to family creature; requires Oracle/hash precheck, ManaLoom review, and approved PG apply before product use.')
),
rule_rows AS (
  SELECT
    r.normalized_name,
    r.logical_rule_key,
    r.oracle_hash,
    r.review_status,
    r.execution_status
  FROM proposed p
  LEFT JOIN public.card_battle_rules r
    ON (
         r.normalized_name = p.normalized_name
         OR r.normalized_name LIKE p.normalized_name || ' // %'
       )
   AND r.logical_rule_key = p.logical_rule_key
)
SELECT
  p.card_name,
  p.normalized_name,
  p.logical_rule_key,
  count(r.*) FILTER (WHERE r.logical_rule_key = p.logical_rule_key) AS promoted_rule_rows,
  count(r.*) FILTER (WHERE r.review_status = 'verified' AND r.execution_status = 'auto') AS promoted_verified_auto_rows,
  count(r.*) FILTER (WHERE r.oracle_hash = p.oracle_hash) AS promoted_oracle_hash_rows,
  (SELECT count(*) FROM manaloom_deploy_audit.pg197_goldspan_dragon_20260625_013356) AS backup_rows
FROM proposed p
LEFT JOIN rule_rows r
  ON r.normalized_name = p.normalized_name
 AND r.logical_rule_key = p.logical_rule_key
 AND r.oracle_hash = p.oracle_hash
GROUP BY p.card_name, p.normalized_name, p.logical_rule_key, p.oracle_hash
ORDER BY p.card_name;
